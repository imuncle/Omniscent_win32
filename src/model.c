void model_generate(void)
{
    g_vtx_count = g_quad_count = 0;
    uint8_t *m = MODEL_TABLE;
    for (int chunk = 0; chunk < 4; chunk++)
    {
        float fp[15][3];
        int16_t ip[15][4];
        const int8_t *vt = VERTEX_TABLE;
        for (int i = 0; i < 15; i++)
        {
            for (int k = 0; k < 3; k++)
            {
                float v = (float)*vt++;
                fp[i][k] = v;
                ip[i][k] = (int16_t)v;
            }
            ip[i][3] = 0x7F00; /* 初始光照值 */
        }
        while (1)
        {
            uint8_t b = *m++;
            if (b == 0xFF)
                break;                                /* chunk 结束标记 */
            int light = ((b << 8) & 0x7000) | 0x0F00; /* 从命令字节提取光照值 */
            /* 处理 6 个面（立方体的6个面） */
            for (int f = 0; f < 6; f++)
            {
                int tex = (f & 1) ? (*m++ & 0x0F) : (*m >> 4);
                if (!tex--)
                    continue;
                model_quad_t *q = &g_quads[g_quad_count++];
                q->idx[4] = (int16_t)tex;
                for (int v = 0; v < 4; v++)
                {
                    int16_t *src = ip[INDEX_TABLE[f * 4 + v] >> 3];
                    /* 顶点去重：遍历已有顶点，匹配 (x,y,z) */
                    int k = 0;
                    for (; k < g_vtx_count; k++)
                    {
                        if (*(int *)src == *(int *)g_vtxs[k].v && src[2] == g_vtxs[k].v[2])
                            break;
                    }
                    if (k == g_vtx_count)
                    { /* 新顶点 */
                        *(int *)g_vtxs[k].v = *(int *)src;
                        *(int *)&g_vtxs[k].v[2] = *(int *)&src[2];
                        g_vtx_count++;
                    }
                    q->idx[v] = (int16_t)k;
                }
            }
            int face = b & 0x0F;
            for (int k = 0; k < 3; k++)
                fp[14][k] += fp[8 + face][k];
            /* 如果设置了旋转标志，对所有顶点进行旋转变换 */
            if (b & 0x80)
            {
                uint8_t r = *m++;
                int16_t angs[3] = {0, 0, 0};
                int ang = (int16_t)(r << 8) & 0xF000;
                if (r & 4)
                    angs[0] = ang; /* X 轴旋转 */
                if (r & 2)
                    angs[1] = ang; /* Y 轴旋转 */
                if (r & 1)
                    angs[2] = ang; /* Z 轴旋转 */
                for (int i = 0; i < 14; i++)
                    rotate_vec3(fp[i], angs);
            }
            /* 更新顶点位置：沿挤出方向移动 */
            for (int i = 0; i < 4; i++)
            {
                int s = INDEX_TABLE[face * 4 + i] >> 3;
                int d = INDEX_TABLE[(face * 4 + i) ^ 7] >> 3;
                *(int *)ip[d] = *(int *)ip[s];
                *(int *)&ip[d][2] = *(int *)&ip[s][2];
                for (int k = 0; k < 3; k++)
                    ip[s][k] = (int16_t)(fp[s][k] + fp[14][k]);
                ip[s][3] = (int16_t)light; /* 写入光照值 */
            }
        }
    }
}

void model_transform(const float cam_pos[3], const float cam_matrix[9]) {
    for (int i = 0; i < g_vtx_count; i++) {
        for (int j = 0; j < 3; j++) {
            g_tvtxs[i * 3 + j] =
                (g_vtxs[i].v[0] - cam_pos[0]) * cam_matrix[j * 3] +
                (g_vtxs[i].v[1] - cam_pos[1]) * cam_matrix[j * 3 + 1] +
                (g_vtxs[i].v[2] - cam_pos[2]) * cam_matrix[j * 3 + 2];
        }
    }
}
