typedef struct {
    int channel;
    int instrument;
    int data_len;
    uint8_t data[MIDI_DATA_MAX];
} midi_channel_t;

typedef struct {
    midi_channel_t ch[MIDI_MAX_CH];
    int idx[MIDI_MAX_CH][2];
    HMIDIOUT hmo;
} midi_state_t;

static midi_state_t ms;

static int decompress_loop(const uint8_t *table, int offset, uint8_t *data, int *dlen) {
    int p = offset;
    while (table[p] != 0x00) {
        if (table[p] >= 0x80) {
            int count = 0x100 - table[p++];
            int q = p;
            for (int i = 0; i < count; i++)
                p = decompress_loop(table, q, data, dlen);
        } else {
            if (*dlen + 2 <= MIDI_DATA_MAX) {
                data[(*dlen)++] = table[p++];
                data[(*dlen)++] = table[p++];
            } else {
                p += 2;
            }
        }
    }
    return p + 1;
}

void midi_init(void) {
    int p = 0;
    int ch_idx = 0;
    ms.hmo = 0;

    while (MIDI_TABLE[p] != 0 && ch_idx < MIDI_MAX_CH) {
        ms.ch[ch_idx].channel = MIDI_TABLE[p++] - 1;
        ms.ch[ch_idx].instrument = MIDI_TABLE[p++];
        int dlen = 0;
        p = decompress_loop(MIDI_TABLE, p, ms.ch[ch_idx].data, &dlen);
        ms.ch[ch_idx].data_len = dlen;
        ch_idx++;
    }
}

void midi_start(void) {
    MMRESULT rc = midiOutOpen(&ms.hmo, MIDI_MAPPER, 0, 0, CALLBACK_NULL);
    if (rc != MMSYSERR_NOERROR) {
        ms.hmo = 0;
    }

    for (int i = 0; i < MIDI_MAX_CH; i++) {
        ms.idx[i][0] = -2;
        ms.idx[i][1] = 0;
        if (ms.hmo) {
            midiOutShortMsg(ms.hmo,
                (DWORD)(0xC0 | ms.ch[i].channel) | (ms.ch[i].instrument << 8));
        }
    }
}

void midi_stop(void) {
    if (ms.hmo) {
        midiOutReset(ms.hmo);
        midiOutClose(ms.hmo);
        ms.hmo = 0;
    }
}

void midi_tick(void)
{
    for (int i = 0; i < MIDI_MAX_CH; i++)
    {
        if (ms.idx[i][0] >= ms.ch[i].data_len)
            continue;
        if (ms.idx[i][1] == 0)
        {
            ms.idx[i][0] += 2;
            if (ms.idx[i][0] > 0 && ms.hmo)
            {
                MMRESULT rc = midiOutShortMsg(ms.hmo,
                                              (DWORD)(0x80 | ms.ch[i].channel) |
                                                  (ms.ch[i].data[ms.idx[i][0] - 2] << 8) | (0x7F << 16));
                if (rc != MMSYSERR_NOERROR)
                {
                    ms.hmo = 0;
                    break;
                }
            }
            if (ms.idx[i][0] >= ms.ch[i].data_len)
                continue;
            ms.idx[i][1] = ms.ch[i].data[ms.idx[i][0] + 1] * 29;
            if (ms.hmo)
            {
                MMRESULT rc = midiOutShortMsg(ms.hmo,
                                              (DWORD)(0x90 | ms.ch[i].channel) |
                                                  (ms.ch[i].data[ms.idx[i][0]] << 8) | (0x7F << 16));
                if (rc != MMSYSERR_NOERROR)
                {
                    ms.hmo = 0;
                    break;
                }
            }
        }
        ms.idx[i][1]--;
    }
}
